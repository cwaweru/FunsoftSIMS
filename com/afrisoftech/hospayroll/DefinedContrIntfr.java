    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int i = 0;
        try {
            
            java.sql.Statement pstmt = connectDB.createStatement();
            java.sql.ResultSet rs = pstmt.executeQuery("select COUNT(description) from defined_contribution");
            while (rs.next()){
                i = rs.getInt(1);
            }
            
            if (i < 1) {
                java.sql.PreparedStatement pstmt2 = connectDB.prepareStatement("insert into defined_contribution values(?,?,?,?,?,?)");
                pstmt2.setObject(1,jTextField1.getText());
                pstmt2.setObject(2,jTextField2.getText());
                pstmt2.setObject(3,jTextField3.getText());
                pstmt2.setObject(4,jTextField4.getText());
                pstmt2.setObject(5,jPasswordField1.getText());
                pstmt2.setObject(6,jPasswordField2.getText());
                pstmt2.executeUpdate();
            } else {
                
                java.sql.PreparedStatement pstmt3 = connectDB.prepareStatement("UPDATE defined_contribution SET description ='"+jTextField1.getText()+"',rate ='"+jTextField2.getText()+"',fixed_amount ='"+jTextField3.getText()+"',personal_relief ='"+jTextField4.getText()+"',name ='"+jPasswordField1.getText()+"',accessobject ='"+jPasswordField2.getText()+"'  WHERE description IS NOT NULL");
                pstmt3.executeUpdate();
                
            }
            jLabel3.setForeground(java.awt.Color.blue);
            jLabel3.setText("Insert successful");
        }   catch(java.sql.SQLException sq){
            javax.swing.JOptionPane.showMessageDialog(this,"ERROR: "+ sq.getMessage(),"Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
            
            System.out.println(sq.getMessage());
            jLabel3.setForeground(java.awt.Color.red);
            jLabel3.setText("Sorry. Insert not successful");
        }  // Add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed
    
