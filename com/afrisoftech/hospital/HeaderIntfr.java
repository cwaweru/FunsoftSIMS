    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            java.sql.PreparedStatement stmt = connectDB.prepareStatement("delete from pb_header");
            // java.sql.ResultSet rset = stmt.executeQuery("delete from pb_header");
            stmt.executeUpdate();
            
            java.sql.PreparedStatement pstmt = connectDB.prepareStatement("insert into pb_header values(?)");
            if(this.jTextArea1.getText().equals("")){
                javax.swing.JOptionPane.showMessageDialog(this, "You must enter Header","Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
            }else{
                pstmt.setString(1,jTextArea1.getText());
            }
            
            pstmt.executeUpdate();
            
            java.sql.PreparedStatement stmt11 = connectDB.prepareStatement("delete from pb_notice");
            // java.sql.ResultSet rset = stmt.executeQuery("delete from pb_header");
            stmt11.executeUpdate();
            
            java.sql.PreparedStatement pstmt1 = connectDB.prepareStatement("insert into pb_notice values(?)");
            if(this.jTextArea11.getText().equals("")){
                javax.swing.JOptionPane.showMessageDialog(this, "You must enter notice","Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
            }else{
                pstmt1.setString(1,jTextArea11.getText());
            }
            
            pstmt1.executeUpdate();
            
            
            
            jLabel3.setForeground(java.awt.Color.blue);
            jLabel3.setText("Insert successful");
        }   catch(java.sql.SQLException sq){
            javax.swing.JOptionPane.showMessageDialog(this, sq.getMessage(),"Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
            
            System.out.println(sq.getMessage());
            jLabel3.setForeground(java.awt.Color.red);
            jLabel3.setText("Sorry. insert not successful");
        } // Add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed
    
