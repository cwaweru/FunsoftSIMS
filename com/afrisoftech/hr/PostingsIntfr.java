    private void newbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newbtnActionPerformed
        try {
            
            connectDB.setAutoCommit(false);
            java.sql.PreparedStatement pstmt = connectDB.prepareStatement("insert into hr.hr_postings (posting_date,duty_station,designation,session,expected_kpis,comment,date,terms,app_time,roles) values(?,?,?,?,?,?,?,?,?,?)");
            for(int i = 0; i < jTable11.getRowCount(); i++){
                if(jTable11.getModel().getValueAt(i,0) !=null){;
                
                
                pstmt.setObject(1,datePicker1.getDate());
                pstmt.setObject(2,jComboBox2.getSelectedItem().toString());
                pstmt.setObject(3,jComboBox3.getSelectedItem().toString());
                pstmt.setString(4,jTable11.getValueAt(i,0).toString());
                pstmt.setString(5,jTable11.getValueAt(i,1).toString());
                pstmt.setObject(6,jTable11.getValueAt(i,2).toString());
                pstmt.setString(7,jTable11.getValueAt(i,3).toString());
                pstmt.setObject(8,jTextArea1.getText());
                pstmt.setObject(9,datePicker2.getDate());
                pstmt.setObject(10,jComboBox1.getSelectedItem().toString());
                
                
                pstmt.executeUpdate();
                connectDB.commit();
                connectDB.setAutoCommit(true);
                javax.swing.JOptionPane.showMessageDialog(this, "Data saved successfully","Confirmation Message",javax.swing.JOptionPane.INFORMATION_MESSAGE);
                
                }
            }
                
                }   catch(java.sql.SQLException sq){
                    javax.swing.JOptionPane.showMessageDialog(this, sq.getMessage(),"Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
                    try {
                        connectDB.rollback();
                    }catch (java.sql.SQLException sql){
                        javax.swing.JOptionPane.showMessageDialog(this,sql.getMessage(),"Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
                    }
                    
                }           // Add your handling code here:
    }//GEN-LAST:event_newbtnActionPerformed
            
            
