    private void newbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newbtnActionPerformed
 try {
            
            connectDB.setAutoCommit(false);
            java.sql.PreparedStatement pstmt = connectDB.prepareStatement("insert into hr.hr_minutes (date,purpose,code,item_no,resolution,implement_date,remarks,ref_no) values(?,?,?,?,?,?,?,?)");
            for (int i = 0; i < jTable1.getRowCount(); i++){
                if (jTable1.getModel().getValueAt(i,0) != null){
                    
                    pstmt.setObject(1,datePicker1.getDate());
                    pstmt.setString(2,jTextField251.getText());
                    pstmt.setString(3,jTextField41.getText());
                    pstmt.setString(4,jTable1.getValueAt(i,0).toString());
                    pstmt.setObject(5,jTable1.getValueAt(i,1).toString());
                    pstmt.setString(6,jTable1.getValueAt(i,2).toString());
                    pstmt.setString(7,jTable1.getValueAt(i,3).toString());
                    pstmt.setString(8,jTable1.getValueAt(i,4).toString());
                   
                    
                    pstmt.executeUpdate();
                    connectDB.commit();
                    connectDB.setAutoCommit(true);
                    javax.swing.JOptionPane.showMessageDialog(this, "Data saved successfully","Confirmation Message",javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    
                }
            }
            
        }   catch(java.sql.SQLException sq){
            javax.swing.JOptionPane.showMessageDialog(this, sq.getMessage(),"Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
            try {
                connectDB.rollback();
            }catch (java.sql.SQLException sql){
                javax.swing.JOptionPane.showMessageDialog(this,sql.getMessage(),"Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
            }
            
        }        // Add your handling         // Add your handling code here:
    }//GEN-LAST:event_newbtnActionPerformed
    
    
