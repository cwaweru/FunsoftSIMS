    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       
        try { 
            connectDB.setAutoCommit(false);
             java.sql.PreparedStatement pstmt = connectDB.prepareStatement("insert into max_loan_allowable values(initcap(?),?,?, ?)");
            pstmt.setString(1,jTextField5.getText());
            pstmt.setString(3,jTextField2.getText());
            pstmt.setString(2,jComboBox1.getSelectedItem().toString());
             pstmt.setString(4,jTextField6.getText());
            pstmt.executeUpdate();
            connectDB.commit();
            connectDB.setAutoCommit(true);
            this.jComboBox1.setSelectedItem(null);     
                 jTextField2.setText("");   
            this.jTextField5.setText("");
            this.jTextField6.setText("");
            jLabel12.setForeground(java.awt.Color.blue);
            jLabel12.setText("Insert successful");
             }   catch(java.sql.SQLException sq){
             javax.swing.JOptionPane.showMessageDialog(this,sq.getMessage(),"Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
              try {
                  connectDB.rollback();
                    }catch (java.sql.SQLException sql){
                    javax.swing.JOptionPane.showMessageDialog(this,sql.getMessage(),"Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
                }
            System.out.println(sq.getMessage());
            jLabel12.setForeground(java.awt.Color.red);
            jLabel12.setText("Sorry. Insert not Successful");
            }  // Add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

