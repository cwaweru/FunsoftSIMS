    public java.lang.Object[] getListofStaffNos() {
        
        java.lang.Object[] listofStaffNos = null;
        
        java.util.Vector listStaffNoVector = new java.util.Vector(1,1);
        
        
        try {
            java.sql.Statement stmt1M = connectDB.createStatement();
            
            java.sql.ResultSet rSet1M = stmt1M.executeQuery("SELECT distinct payroll_no::int4 FROM member where membership_type != 'SACCO STAFF' and payroll_no != 'NA' and payroll_no != '' and payroll_no notnull and bbf_status ilike 'join bbf'");
            
            while (rSet1M.next()) {
                
                java.lang.Object mtype = rSet1M.getObject(1).toString();
                
                
                java.sql.Statement stmt1 = connectDB.createStatement();
                
                java.sql.ResultSet rSet1 = stmt1.executeQuery("SELECT distinct payroll_no FROM bbf_deductions_view WHERE payroll_no ilike '"+mtype+"' ORDER BY payroll_no");
                
                while (rSet1.next()) {
                    
                    listStaffNoVector.addElement(rSet1.getObject(1).toString());
                    
                }
                
            }
        }catch (java.sql.SQLException sqlExec) {
            sqlExec.printStackTrace();
            
            javax.swing.JOptionPane.showMessageDialog(new javax.swing.JFrame(), sqlExec.getMessage());
            
        }
        
        listofStaffNos = listStaffNoVector.toArray();
        System.out.println("Done list of Staff Nos ...");
        return listofStaffNos;
    }
